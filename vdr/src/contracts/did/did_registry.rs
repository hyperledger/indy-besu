use crate::{
    client::{ContractParam, LedgerClient, Transaction, TransactionType},
    contracts::did::did_doc::{DidDocument, DidDocumentWithMeta},
    error::{VdrError, VdrResult},
};

pub struct DidRegistry;

impl DidRegistry {
    const CONTRACT_NAME: &'static str = "DidRegistry";
    const METHOD_CREATE_DID: &'static str = "createDid";
    const _METHOD_UPDATE_DID: &'static str = "updateDid";
    const _METHOD_DEACTIVATE_DID: &'static str = "deactivateDid";
    const METHOD_RESOLVE_DID: &'static str = "resolveDid";

    pub fn build_create_did_transaction(
        client: &LedgerClient,
        from: &str,
        did_doc: &DidDocument,
    ) -> VdrResult<Transaction> {
        let contract = client.contract(DidRegistry::CONTRACT_NAME)?;

        let params: Vec<ContractParam> = vec![did_doc.clone().into()];
        let data = contract.encode_input(Self::METHOD_CREATE_DID, &params)?;

        let transaction = Transaction {
            type_: TransactionType::Write,
            chain_id: client.chain_id(),
            from: Some(from.to_string()),
            to: contract.address(),
            data,
            signed: None,
        };
        Ok(transaction)
    }

    pub fn build_resolve_did_transaction(
        client: &LedgerClient,
        did: &str,
    ) -> VdrResult<Transaction> {
        let contract = client.contract(Self::CONTRACT_NAME)?;

        let params: Vec<ContractParam> = vec![ContractParam::String(did.to_string())];
        let data = contract.encode_input(Self::METHOD_RESOLVE_DID, &params)?;

        let transaction = Transaction {
            type_: TransactionType::Read,
            chain_id: client.chain_id(),
            from: None,
            to: contract.address(),
            data,
            signed: None,
        };
        Ok(transaction)
    }

    pub fn parse_resolve_did_result(client: &LedgerClient, bytes: &[u8]) -> VdrResult<DidDocument> {
        let contract = client.contract(Self::CONTRACT_NAME)?;
        let output = contract.decode_output(Self::METHOD_RESOLVE_DID, bytes)?;
        if output.is_empty() {
            return Err(VdrError::Common(
                "Unable to parse DidDocument: Empty data".to_string(),
            ));
        }

        let data = output.get_tuple(0)?;
        let did_with_meta = DidDocumentWithMeta::try_from(data)?;
        Ok(did_with_meta.document)
    }

    pub async fn create_did(
        client: &LedgerClient,
        from: &str,
        did_doc: &DidDocument,
    ) -> VdrResult<Vec<u8>> {
        let transaction = Self::build_create_did_transaction(client, from, did_doc)?;
        let signed_transaction = client.sign_transaction(&transaction).await?;
        client.submit_transaction(&signed_transaction).await
    }

    pub async fn resolve_did(client: &LedgerClient, did: &str) -> VdrResult<DidDocument> {
        let transaction = Self::build_resolve_did_transaction(client, did)?;
        let result = client.submit_transaction(&transaction).await?;
        Self::parse_resolve_did_result(client, &result)
    }
}

